// Clase WebSocket Client mejorada
class GameWebSocketClient {
  constructor() {
    this.ws = null;
    this.matchId = null;
    this.playerId = null;
    this.token = null;
    this.isConnected = false;
    this.connectionAttempts = 0;
    this.maxRetries = 3;
  }

  connect(matchId, token, playerId) {
    this.matchId = matchId;
    this.token = token;
    this.playerId = playerId;
    this.connectionAttempts++;
    
    // Construir URL con token si est√° disponible
    let wsUrl = `ws://localhost:8000/ws/games/${matchId}`;
    if (token) {
      wsUrl += `?token=${encodeURIComponent(token)}`;
    }
    
    this.log(`üîó Intento ${this.connectionAttempts}: Conectando a WebSocket`);
    this.log(`üîå URL: ${wsUrl.replace(/token=[^&]+/, 'token=***')}`);

    try {
      this.ws = new WebSocket(wsUrl);
      this.setupEventHandlers();
    } catch (error) {
      this.log(`‚ùå Error al crear WebSocket: ${error.message}`);
      this.handleConnectionError();
    }
  }

  setupEventHandlers() {
    this.ws.onopen = () => {
      this.log("‚úÖ Conectado al servidor WebSocket");
      this.isConnected = true;
      this.connectionAttempts = 0;
      isOnlineMode = true;
      updateConnectionStatusEnhanced("connected", "üü¢ Conectado");
      updateMessage(`üëã Hola ${userInfo.user.email}`, "");
      
      // Unirse al juego autom√°ticamente
      this.joinGame();
    };

    this.ws.onmessage = (event) => {
      const data = JSON.parse(event.data);
      this.log(`üì• Mensaje recibido: ${data.type}`);
      this.log(`üìã Contenido: ${JSON.stringify(data, null, 2)}`);
      this.handleMessage(data);
    };

    this.ws.onclose = (event) => {
      this.log(`‚ùå Desconectado. C√≥digo: ${event.code}, Raz√≥n: ${event.reason || 'Sin raz√≥n'}`);
      this.isConnected = false;
      isOnlineMode = false;
      updateConnectionStatusEnhanced("disconnected", "üî¥ Desconectado");
      
      // Reintentar conexi√≥n si no fue intencional
      if (event.code !== 1000 && this.connectionAttempts < this.maxRetries) {
        setTimeout(() => {
          this.log(`üîÑ Reintentando conexi√≥n (${this.connectionAttempts + 1}/${this.maxRetries})`);
          this.connect(this.matchId, this.token, this.playerId);
        }, 3000);
      }
    };

    this.ws.onerror = (error) => {
      this.log(`üí• Error WebSocket: ${error.message || 'Error desconocido'}`);
      this.handleConnectionError();
    };
  }

  joinGame() {
    if (!this.isConnected) return;
    
    const joinMessage = {
      type: "join_game",
      match_id: this.matchId,
      player_id: this.playerId,
      game_type: "connect4"
    };
    
    this.sendMessage(joinMessage);
    this.log(`üì§ Enviando join_game para ${this.playerId}`);
  }

  makeMove(column) {
    if (!this.isConnected || !isMyTurn) {
      this.log(`‚ùå No se puede hacer movimiento: Connected=${this.isConnected}, MyTurn=${isMyTurn}`);
      return false;
    }

    const moveMessage = {
      type: "make_move",
      player_id: this.playerId,
      move: { column: column }
    };
    
    this.sendMessage(moveMessage);
    this.log(`üì§ Movimiento enviado: columna ${column}`);
    return true;
  }

  sendMessage(message) {
    if (this.ws && this.ws.readyState === WebSocket.OPEN) {
      this.ws.send(JSON.stringify(message));
    } else {
      this.log("‚ùå No se puede enviar mensaje: WebSocket no conectado");
    }
  }

  handleMessage(data) {
    // Usar las funciones existentes del juego
    handleWebSocketMessage(data);
    
    // Logging adicional
    switch (data.type) {
      case "game_state":
        this.log(`üéÆ Estado: ${data.state}, Color: ${data.player_color}`);
        break;
      case "move_made":
        this.log(`üéØ Movimiento: ${data.player_symbol} en columna ${data.move?.column}`);
        break;
      case "error":
        this.log(`üí• Error del servidor: ${data.message}`);
        break;
    }
  }

  handleConnectionError() {
    isOnlineMode = false;
    updateConnectionStatusEnhanced("disconnected", "üî¥ Error de conexi√≥n");
    updateMessage("‚ùå Error de conexi√≥n al servidor", "");
  }

  log(message) {
    const timestamp = new Date().toLocaleTimeString();
    console.log(`[${timestamp}] ${message}`);
  }

  disconnect() {
    if (this.ws) {
      this.ws.close(1000, "Desconexi√≥n manual");
      this.isConnected = false;
    }
  }

  // M√©todos adicionales para compatibilidad
  restartGame() {
    if (!this.isConnected) return;
    this.sendMessage({ type: "restart_game" });
    this.log("üì§ Solicitud de reinicio enviada");
  }

  getGameState() {
    if (!this.isConnected) return;
    this.sendMessage({ type: "get_game_state" });
    this.log("üì§ Solicitando estado del juego");
  }
}

