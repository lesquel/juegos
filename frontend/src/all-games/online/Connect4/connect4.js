// Funci√≥n para extraer el user_id del token JWT
function extractUserIdFromToken(token) {
  if (!token) return null;
  
  try {
    // Los JWT tienen 3 partes separadas por puntos: header.payload.signature
    const parts = token.split('.');
    if (parts.length !== 3) {
      console.error("‚ùå Token JWT malformado");
      return null;
    }
    
    // Decodificar el payload (segunda parte)
    const payload = parts[1];
    
    // Agregar padding si es necesario para base64
    const paddedPayload = payload + '='.repeat((4 - payload.length % 4) % 4);
    
    // Decodificar de base64
    const decodedPayload = JSON.parse(atob(paddedPayload));
    
    console.log("üîì Token JWT decodificado:", decodedPayload);
    
    // Extraer el subject (user_id)
    const userId = decodedPayload.sub;
    
    if (userId) {
      console.log("‚úÖ User ID extra√≠do del token:", userId);
      return userId;
    } else {
      console.error("‚ùå No se encontr√≥ 'sub' en el token");
      return null;
    }
    
  } catch (error) {
    console.error("üí• Error al decodificar el token JWT:", error);
    return null;
  }
}

